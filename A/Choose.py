# 第一层：宏观筛选（市场环境判断）
# 目标：确定当前市场风格和热点方向
# 数据源使用：
#
# 财经快讯 + 市场动态
# 行业板块涨幅排行
# 概念板块涨幅排行
# 资金流向（市场整体）
# 筛选标准：
#
# 市场整体趋势
#
# 大盘指数（上证、深证）近5日/20日走势
# 市场成交量是否放大
# 涨跌家数比例
# 热点板块识别（选2-3个）
#
# 近3日板块涨幅 > 5%
# 板块内涨停股数量 ≥ 3只
# 板块资金净流入 > 5亿
# 政策催化剂
#
# 关注财经快讯中的政策消息
# 股市日历中的重大事件
# 输出结果： 锁定2-3个强势板块
#
# 第二层：技术筛选（个股精选）
# 目标：在热点板块中找到最强个股
# 数据源使用：
#
# 板块成份股列表
# 技术指标（量价齐升、创新高）
# 历史行情数据（后复权）
# 盘口异动
# 大宗交易
# 筛选条件（必须同时满足）：
#
# A. 技术形态（核心）
# <TEXT>
# ✓ 量价齐升：近3日成交量逐日放大 + 股价上涨
# ✓ 创新高：突破近60日/120日高点
# ✓ 均线多头排列：5日 > 10日 > 20日 > 60日
# ✓ 回调幅度：如果是回调买入，回调幅度 < 20%
# B. 资金面
# <TEXT>
# ✓ 近5日主力资金净流入 > 0
# ✓ 机构参与度（千股千评）：中度或高度
# ✓ 大宗交易：如有活跃营业部买入加分
# C. 热度面
# <TEXT>
# ✓ 人气榜-A股排名前50
# ✓ 或飙升榜上榜（关注度急升）
# D. 流动性
# <TEXT>
# ✓ 日均成交额 > 1亿（避免流动性不足）
# ✓ 换手率：3% - 15%（过低不活跃，过高可能炒作过度）
# 数量控制： 筛选出5-10只候选股
#
# 第三层：风险过滤（排雷）
# 目标：剔除高风险标的
# 数据源使用：
#
# 风险警示板
# 千股千评详情
# 财务数据
# 股市日历
# 排除标准（一票否决）：
#
# <TEXT>
# ✗ ST股、*ST股（风险警示板）
# ✗ 退市风险股
# ✗ 近期有重大诉讼、业绩暴雷
# ✗ 股价连续涨停超过5个（追高风险大）
# ✗ 市盈率 < 0（亏损企业）或 > 100（高估值风险）
# ✗ 近期有大股东减持公告
# ✗ 机构参与度为"无"
# 加分项：
#
# <TEXT>
# ✓ 近期有利好公告（股市日历）
# ✓ 业绩预增、高送转预期
# ✓ 机构调研频繁
# ✓ 北向资金持续流入
# 输出结果： 最终标的池 3-5只
#
# 第四层：动态跟踪系统
# 买入策略
# 时机选择：
#
# 激进型：技术突破当日，量价齐升时买入
# 稳健型：等待首次回调，在10日均线附近买入
# 仓位管理：
#
# 单只股票 ≤ 总资金的20%
# 分批买入：首次50%，确认趋势后加仓30%，预留20%备用
# 买入确认清单：
#
# <TEXT>
# □ 大盘环境良好（不在大跌中）
# □ 板块热度持续
# □ 个股技术形态完好
# □ 当日盘口无异常大单砸盘
# □ 成交量配合
# 持仓监控
# 每日必做：
#
# 查看个股盘口异动
# 关注资金流向变化
# 跟踪板块热度是否降温
# 浏览财经快讯（政策风险）
# 预警信号（考虑减仓）：
#
# <TEXT>
# ⚠ 成交量萎缩 + 股价滞涨（量价背离）
# ⚠ 主力资金连续3日净流出
# ⚠ 板块整体走弱
# ⚠ 跌破10日均线
# ⚠ 出现利空消息
# 卖出策略
# 止盈：
#
# 目标1：涨幅达15-20%，卖出50%
# 目标2：涨幅达30%以上，卖出剩余30%，保留20%
# 趋势型：跌破10日均线清仓
# 止损（纪律性执行）：
#
# 跌破买入价7-10%，无条件止损
# 或跌破重要支撑位（如20日均线）
# 其他卖出信号：
#
# 板块热度消退，涨停板数量锐减
# 出现重大利空消息
# 技术形态破坏（破位、死叉）
# 📱 实战操作流程（每日）
# 开盘前（9:00-9:25）
# 查看财经快讯，了解隔夜消息
# 查看板块异动预告
# 查看持仓股票有无公告
# 制定当日操作计划
# 盘中（9:30-15:00）
# 监控涨停板行情（10:00首次查看）
# 观察板块异动详情
# 跟踪持仓股盘口异动
# 关注人气榜、飙升榜变化
# 收盘后（15:00-21:00）
# 统计当日涨停板分析
# 查看资金流向排名
# 运行选股系统，更新候选池
# 查看千股千评机构观点
# 阅读财经内容精选
# 记录交易日志
# 🛠️ 系统优化建议
# 定期复盘（每周）
# 统计本周选股成功率
# 分析失败案例原因
# 调整筛选参数（如量能阈值）
# 总结市场风格变化
# 参数动态调整
# 不同市场环境下调整标准：
#
# 牛市：适当放宽筛选条件，提高仓位
# 熊市：严格风控，降低仓位，只做超跌反弹
# 震荡市：快进快出，止盈止损更严格
# ⚠️ 关键纪律（必须遵守）
# 严格止损：亏损达到设定比例必须离场
# 不追涨停板：涨停后不追高
# 不满仓操作：保留至少30%现金
# 不频繁交易：每周交易次数 ≤ 3次
# 不听小道消息：只依据系统信号
# 记录每一笔交易：便于复盘改进
# 💡 特别提醒
# 这套系统的核心是：
#
# 趋势为王：顺势而为，不逆市操作
# 纪律第一：严格执行买卖纪律
# 风控至上：保护本金比盈利更重要
# 持续学习：不断优化和调整
# 股市投资有风险，这套系统只是工具，无法保证盈利。建议：
#
# 先用模拟盘测试至少1个月
# 小资金实盘验证2-3个月
# 根据实际效果调整参数
